package pojoClassforParse;

public class SpouseDetails {
	String name;
	long phno;
	public SpouseDetails(String name, long phno) {
		super();
		this.name = name;
		this.phno = phno;
		
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public long getPhno() {
		return phno;
	}
	public void setPhno(long phno) {
		this.phno = phno;
	}
	

}

